#include<iostream>
using namespace std;
int main()
{
    int a = 5;
    int b = 10;

    cout<<"Pre-increment of a: "<<++a<<endl;

    cout<<"Pre-decrement of b: "<<--b<<endl;

    cout<<"Unary minus of a: "<<-a<<endl;

    bool x = true;
    cout<<"Logical NOT of x is "<<!x<<endl;

    return 0;

}